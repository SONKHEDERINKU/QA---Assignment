package PacticePROGRAM;

public class SWITCH2 {

	public static void main(String[] args) {
		int colour=4;
		switch(colour)
		{
	
		case 1:System.out.println("blue");break;
		case 2 : System.out.println("black");break;
		case 3 : System.out.println("green");break;
		case 4 : System.out.println("red");break;
		default : System.out.println("no colour");

	}

}
}